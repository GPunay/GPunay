
#Un valor en range
for i in range (10):
    print(i)

print("------------------")
#Dos valores en range
for i in range (1, 10):
    print(i)

print("------------------")
#Tres valores en range
for i in range (1, 10, 2):
    print(i)

print("------------------")
for i in range (10, 0, -1):
    print(i)
